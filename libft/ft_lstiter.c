#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*tmp;

	tmp = lst;
	f(tmp);
	if (tmp->next != NULL)
		ft_lstiter(tmp->next, f);
}
