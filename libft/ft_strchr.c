#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*dst;

	dst = (char*)s;
	while (*dst != c)
	{
		if (*dst == '\0')
			return (NULL);
		dst++;
	}
	return (dst);
}
