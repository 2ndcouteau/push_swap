#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	a;
	char			*s1;

	a = 0;
	s1 = (char *)s;
	while (a < n && s)
	{
		s1[a] = 0;
		a++;
	}
}
