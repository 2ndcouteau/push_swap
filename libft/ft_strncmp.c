#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t a;

	a = 0;
	while (a < n)
	{
		if (((unsigned char *)s1)[a] == '\0' &&\
			((unsigned char *)s2)[a] == '\0')
			return (0);
		if (((unsigned char *)s1)[a] != ((unsigned char *)s2)[a])
			return (((unsigned char *)s1)[a] - ((unsigned char *)s2)[a]);
		a++;
	}
	return (0);
}
