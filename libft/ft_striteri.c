#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int n;

	if (s != NULL && f != NULL)
	{
		n = 0;
		while (s[n] != '\0')
		{
			f(n, &(s[n]));
			n++;
		}
	}
}
